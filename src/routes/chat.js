/**
 * Determine source attribution based on retrieved chunks
 * @param {Array} searchResults - Array of search results from Qdrant
 * @param {string} unitName - Current unit name
 * @param {boolean} sourceDownloadsEnabled - Whether source document downloads are enabled for the course
 * @returns {Promise<Object>} Source attribution information
 */
async function determineSourceAttribution(searchResults, unitName, sourceDownloadsEnabled = false) {
    try {
        // If no search results, content is from GPT
        if (!searchResults || searchResults.length === 0) {
            return {
                source: 'GPT',
                description: 'Generated by AI (no relevant course materials found)',
                unitName: null,
                documentType: null,
                downloadsEnabled: false,
                documents: []
            };
        }

        // Check if search results are relevant enough (low scores indicate poor matches)
        const scores = searchResults.map(result => (result.score || 0));
        const maxScore = Math.max(...scores);
        const top3Scores = [...scores].sort((a, b) => b - a).slice(0, 3);
        const avgTop3Score = top3Scores.length > 0
            ? top3Scores.reduce((sum, score) => sum + score, 0) / top3Scores.length
            : 0;

        console.log('🔍 [SOURCE_DEBUG] Search relevance - avgTop3Score:', avgTop3Score, 'maxScore:', maxScore);

        // Treat as GPT only when both max and top-hit average are weak.
        if (avgTop3Score < 0.10 && maxScore < 0.18) {
            console.log('🔍 [SOURCE_DEBUG] Low relevance scores - treating as GPT');
            return {
                source: 'GPT',
                description: 'Generated by AI (no relevant course materials found)',
                unitName: null,
                documentType: null,
                downloadsEnabled: false,
                documents: []
            };
        }

        // Filter for meaningful chunks
        const relevantChunks = searchResults.filter(result => (result.score || 0) > 0.10);

        if (relevantChunks.length === 0) {
             return {
                source: 'GPT',
                description: 'Generated by AI (no relevant course materials found)',
                unitName: null,
                documentType: null,
                downloadsEnabled: false,
                documents: []
            };
        }

        // Collect unique sources
        const sources = new Set();
        const sourceDocuments = new Map();
        
        relevantChunks.forEach(chunk => {
            const docType = chunk.type || chunk.documentType || 'unknown';
            const sourceUnit = chunk.lectureName || unitName;
            const score = typeof chunk.score === 'number' ? chunk.score : 0;
            
            let readableType = 'Material';
            switch (docType) {
                case 'lecture_notes': readableType = 'Lecture Notes'; break;
                case 'practice_q_tutorials':
                case 'practice-quiz':
                    readableType = 'Practice Questions';
                    break;
                case 'additional': readableType = 'Additional Materials'; break;
                default: readableType = 'Course Material';
            }

            sources.add(`${readableType} (${sourceUnit})`);

            const rawDocumentId = typeof chunk.documentId === 'string' ? chunk.documentId.trim() : '';
            const documentId = rawDocumentId || null;
            // Deduplicate by type + unit (not fileName, which is inconsistent across chunks)
            const dedupeKey = `${readableType}::${sourceUnit}`;
            const existing = sourceDocuments.get(dedupeKey);

            if (!existing) {
                sourceDocuments.set(dedupeKey, {
                    documentId,
                    fileName: readableType,
                    lectureName: sourceUnit || unitName || null,
                    maxScore: score
                });
            } else {
                if (score > existing.maxScore) {
                    existing.maxScore = score;
                    // Prefer documentId from highest-scoring chunk (most likely the newest valid upload)
                    if (documentId) {
                        existing.documentId = documentId;
                    }
                } else if (!existing.documentId && documentId) {
                    existing.documentId = documentId;
                }
            }
        });

        // Debug: Log details about practice quiz chunks specifically
        const practiceQuizChunks = searchResults.filter(r => r.type === 'practice_q_tutorials' || r.documentType === 'practice-quiz');
        if (practiceQuizChunks.length > 0) {
            console.log('🔍 [SOURCE_DEBUG] Practice quiz chunks found:', practiceQuizChunks.length);
        }

        const sortedSourceDocs = Array.from(sourceDocuments.values())
            .sort((a, b) => b.maxScore - a.maxScore);
        const sourceDescription = sortedSourceDocs.length > 0
            ? sortedSourceDocs
                .map(doc => `${doc.fileName}${doc.lectureName ? ` (${doc.lectureName})` : ''}`)
                .join(', ')
            : Array.from(sources).join(', ');
        console.log('🔍 [SOURCE_DEBUG] Final Source Description:', sourceDescription);

        const downloadableDocuments = sortedSourceDocs
            .filter(doc => !!doc.documentId)
            .map(doc => ({
                documentId: doc.documentId,
                fileName: doc.fileName,
                lectureName: doc.lectureName
            }));

        return {
            source: 'multiple',
            description: `From: ${sourceDescription || 'Course Material'}`,
            unitName: unitName,
            documentType: 'multiple',
            downloadsEnabled: !!sourceDownloadsEnabled,
            documents: sourceDownloadsEnabled ? downloadableDocuments : []
        };

    } catch (error) {
        console.error('Error determining source attribution:', error);
        return {
            source: 'GPT',
            description: 'Generated by AI (error determining source)',
            unitName: null,
            documentType: null,
            downloadsEnabled: false,
            documents: []
        };
    }
}

function inferExtensionFromMimeType(mimeType) {
    switch ((mimeType || '').toLowerCase()) {
        case 'application/pdf':
            return '.pdf';
        case 'text/markdown':
            return '.md';
        case 'application/msword':
            return '.doc';
        case 'application/vnd.openxmlformats-officedocument.wordprocessingml.document':
            return '.docx';
        case 'application/vnd.openxmlformats-officedocument.presentationml.presentation':
            return '.pptx';
        case 'application/rtf':
            return '.rtf';
        case 'text/plain':
            return '.txt';
        default:
            return '';
    }
}

function resolveDownloadFilename(document) {
    const fallbackName = `source-${document.documentId || Date.now()}`;
    const rawOriginal = (document.originalName || '').trim();
    const rawFile = (document.filename || '').trim();
    const preferredName = rawOriginal || rawFile || fallbackName;
    let safeName = path.basename(preferredName).replace(/[\r\n]/g, '');

    if (!path.extname(safeName)) {
        if (rawFile && path.extname(rawFile)) {
            safeName = path.basename(rawFile).replace(/[\r\n]/g, '');
        } else {
            safeName += inferExtensionFromMimeType(document.mimeType);
        }
    }

    return safeName || `${fallbackName}.txt`;
}

function setAttachmentHeaders(res, filename) {
    const encodedName = encodeURIComponent(filename);
    const asciiFallback = filename.replace(/[^\x20-\x7E]/g, '_').replace(/"/g, '');
    res.setHeader(
        'Content-Disposition',
        `attachment; filename="${asciiFallback}"; filename*=UTF-8''${encodedName}`
    );
}

/**
 * Analyze chunk sources to determine document types
 * @param {Array} searchResults - Array of search results
 * @returns {Object} Analysis of chunk sources
 */
function analyzeChunkSources(searchResults) {
    const analysis = {
        hasLectureNotes: false,
        hasAdditionalMaterials: false,
        hasPracticeQuiz: false,
        hasUnknownType: false
    };

    for (const result of searchResults) {
        const docType = result.type || result.documentType || 'unknown';
        console.log('🔍 [SOURCE_DEBUG] Analyzing chunk with docType:', docType, 'fileName:', result.fileName);

        switch (docType) {
            case 'lecture_notes':
                analysis.hasLectureNotes = true;
                break;
            case 'additional':
                analysis.hasAdditionalMaterials = true;
                break;
            case 'practice_q_tutorials':
                analysis.hasPracticeQuiz = true;
                break;
            case 'unknown':
                analysis.hasUnknownType = true;
                // Try to infer type from filename for legacy documents
                const fileName = (result.fileName || '').toLowerCase();
                console.log('🔍 [SOURCE_DEBUG] Inferring type from filename:', fileName);

                // Be more specific about lecture notes inference
                if (fileName.includes('lecture') && (fileName.includes('notes') || fileName.includes('note'))) {
                    analysis.hasLectureNotes = true;
                } else if (fileName.includes('practice') || fileName.includes('quiz') || fileName.includes('tutorial')) {
                    analysis.hasPracticeQuiz = true;
                } else {
                    // Default to additional materials for unknown types
                    analysis.hasAdditionalMaterials = true;
                }
                break;
        }
    }

    return analysis;
}

