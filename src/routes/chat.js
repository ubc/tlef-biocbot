    const hasCourseAccess = await CourseModel.userHasCourseAccess(db, courseId, user.userId, user.role);
    if (!hasCourseAccess) return false;

    if (user.role === 'ta') {
        return CourseModel.checkTAPermission(db, courseId, user.userId, 'flags');
    }

