/**
 * GET /api/chat/source-documents/:documentId/download
 * Download a source-attributed document when enabled for the course
 */
router.get('/source-documents/:documentId/download', async (req, res) => {
    try {
        const { documentId } = req.params;
        const { courseId } = req.query;

        if (!documentId || !courseId) {
            return res.status(400).json({ success: false, message: 'Missing documentId or courseId' });
        }

        const user = req.user;
        if (!user) {
            return res.status(401).json({ success: false, message: 'Authentication required' });
        }

        const db = req.app.locals.db;
        if (!db) {
            return res.status(503).json({ success: false, message: 'Database connection not available' });
        }

        const course = await db.collection('courses').findOne(
            { courseId },
            { projection: { courseId: 1, quizSettings: 1 } }
        );
        if (!course) {
            return res.status(404).json({ success: false, message: 'Course not found' });
        }

        const downloadsEnabled = !!(course.quizSettings && course.quizSettings.allowSourceAttributionDownloads);
        if (!downloadsEnabled) {
            return res.status(403).json({ success: false, message: 'Source attribution downloads are not enabled for this course' });
        }

        let hasAccess = false;
        if (user.role === 'student') {
            const enrollment = await CourseModel.getStudentEnrollment(db, courseId, user.userId);
            hasAccess = !!(enrollment.success && enrollment.enrolled);
        } else if (user.role === 'instructor' || user.role === 'ta') {
            hasAccess = await CourseModel.userHasCourseAccess(db, courseId, user.userId, user.role);
        }

        if (!hasAccess) {
            return res.status(403).json({ success: false, message: 'Access denied for this course' });
        }

        const document = await DocumentModel.getDocumentById(db, documentId);
        if (!document || document.courseId !== courseId) {
            return res.status(404).json({ success: false, message: 'Document not found' });
        }

        const downloadFilename = resolveDownloadFilename(document);
        setAttachmentHeaders(res, downloadFilename);

        const isFileDocument = document.contentType === 'file'
            || ((!!document.fileData || !!document.fileId) && document.contentType !== 'text');

        if (isFileDocument) {
            // Newer uploads keep the binary in GridFS (referenced by fileId); older
            // documents may still have it inline in fileData. Support both so the
            // student gets the original file (e.g. the full PowerPoint), not the
            // extracted text.
            if (document.fileId) {
                res.setHeader('Content-Type', document.mimeType || 'application/octet-stream');
                return gridfs.openDownloadStream(db, document.fileId)
                    .on('error', (err) => {
                        console.error(`❌ GridFS download failed for source document ${documentId}:`, err.message);
                        if (!res.headersSent) {
                            res.status(500).json({ success: false, message: 'Stored file could not be read' });
                        } else {
                            res.end();
                        }
                    })
                    .pipe(res);
            }

            const payload = Buffer.isBuffer(document.fileData)
                ? document.fileData
                : (document.fileData && document.fileData.buffer ? Buffer.from(document.fileData.buffer) : null);

            if (!payload) {
                return res.status(500).json({ success: false, message: 'Stored file data is invalid' });
            }

            res.setHeader('Content-Type', document.mimeType || 'application/octet-stream');
            return res.send(payload);
        }

        const textContent = typeof document.content === 'string' ? document.content : '';
        res.setHeader('Content-Type', `${document.mimeType || 'text/plain'}; charset=utf-8`);
        return res.send(textContent);
    } catch (error) {
        console.error('Error downloading source-attributed document:', error);
        res.status(500).json({ success: false, message: 'Failed to download source document' });
    }
});

